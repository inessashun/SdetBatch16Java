package class7WhileForLoop;

public class E3WhileLoopPrint5Times {
    public static void main(String[] args) {
        //we want to print "Hello World" 5 times on the console
         int counter=1;
        // this condition will return as a true 5 times
         while(counter<=5){
             System.out.println("Hello World");
             //in every iteration the value of the counter variable will increase
             counter++;//5
         }
    }
}
