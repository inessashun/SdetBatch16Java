package class7WhileForLoop;

public class E7WhileLoop {
    public static void main(String[] args) {
            //print numbers in one line 3 5 7 9 11 13 using a while loop
         int num=3;
         while(num<=13){ //same as num<14
             System.out.print(num+" ");
             num+=2; //num=num+2 num++ num++
         }
    }
}
