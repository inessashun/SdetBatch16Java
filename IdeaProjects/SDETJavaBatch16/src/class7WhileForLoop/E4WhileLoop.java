package class7WhileForLoop;

public class E4WhileLoop {
    public static void main(String[] args) {
        //write a loop that prints numbers from 1 to 5

        /* SOLUTION:
        starting point => 1
        how much is each step => 1
        ending point =>5
        */

         int start=1; //starting point
         while(start<=5){ //ending point
             System.out.println(start);
             start++;
         }
    }
}
