package class7WhileForLoop;

public class E14ForLoop2Ways {
    public static void main(String[] args) {

         int i=1;
         while(i<5) {
             System.out.println(i);
            i++;
         }
        System.out.println("********");  //same code just different ways
         for(int j=1; j<5; j++){
             System.out.println(j);

         }
    }
}

