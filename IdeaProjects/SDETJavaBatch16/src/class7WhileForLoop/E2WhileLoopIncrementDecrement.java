package class7WhileForLoop;

public class E2WhileLoopIncrementDecrement {
    public static void main(String[] args) {

     int num=0;
        // below we are using the increment operator
     num++; // 0-> 1
     num++; // 1->2 //increment 0 in 2 times
        System.out.println(num);
        int num2=10;
        num2--; //decrement 10 in 1 time
        System.out.println(num2);
    }
}
