package class4IfElseScanner;

public class E1IfElseStatement {
    //for Windows we can use CTRL+ALT+l to format code lines
    //for Mac cmd+opt+l
    public static void main(String[] args) {
        boolean hungry=true; //if false the Java executes the sout from else line
        if(hungry) {
            System.out.println("Lets eat something");
            System.out.println("Lets cook new dishes");
        }else{
            System.out.println("Lets go for a walk");
        }
    }
}
