package class8WhileNestedLoop;

public class E6NestedForLoopRows3TimesColumns {
    public static void main(String[] args) {
        for (int i=0; i<5; i++){
            System.out.print(i+" ");
        }
        System.out.println(); //empty line to separate code
        for (int i=0; i<5; i++){
            System.out.print(i+" ");
        }
        System.out.println(); //empty line to separate code
        for (int i=0; i<5; i++){
            System.out.print(i+" ");
        }
    }
}
