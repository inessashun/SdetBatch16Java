package class8WhileNestedLoop;

public class E8NestedForLoopRowsPaternColumns {
    public static void main(String[] args) {

        for (int i=0; i<5; i++){
            System.out.print("* "); //row 1
        }
        System.out.println();
        for (int i=0; i<5; i++){
            System.out.print("# "); //row 2
        }
        System.out.println();
        for (int i=0; i<5; i++){
            System.out.print("@ "); //row 3
        }
    }
}
