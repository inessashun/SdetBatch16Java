package class5IfElseLogical;

public class E5LogicalOperators {
    public static void main(String[] args) {
        /*
        NOT ! it flips boolean values for example if the value is true
        it will change it to false and if it is false it will change it to true
         */
        boolean flag=true; // => ! only for boolean
        // ! operator will reverse the condition
        // and it will get printed
        System.out.println(!flag);
        System.out.println(flag);

        String name="Semir";
        // only works with boolean data type
        //  System.out.println(!name);
    }
}
