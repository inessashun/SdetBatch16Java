package class6LogicalSwitch;

public class E6SwitchStatement {
    public static void main(String[] args) {

        String name="Kevin";
        switch(name){
            case "Kevin":
                System.out.println("Miami");
                System.out.println(10+23);
                break;
            case "Jack":
                System.out.println("Portland");
                break;
            case"Axel":
                System.out.println("Va");

        }
    }
}
