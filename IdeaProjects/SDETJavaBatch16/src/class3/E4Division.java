package class3;

public class E4Division {
    public static void main(String[] args) {

        int number1=10;
        int number2=5;
        int sum=number1/number2;
        System.out.println(sum);
        System.out.println(number1/number2);
        System.out.println(10/5);
        System.out.println("**********");
        System.out.println(10/5);
        //if the data type is int we wil not see the decimal part
        System.out.println(11/5);
        //if the data type will be a double/float we will see the decimal part in answer
        System.out.println(11.0/5.0);
        int num1=10;
        int num2=4;
        double num3=10;
        double num4=4;
        System.out.println(num1/num2);
        System.out.println(num3/num4);


    }
}
