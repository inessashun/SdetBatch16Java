package class3;

public class E20IfElseStatements {
    public static void main(String[] args) {
        //If inside parenthesis is True, Java will Print it, if False - ignore.
        int accountBalance=1950;
        if(accountBalance>2000)
        {
            System.out.println("Let's go shopping");
        }
        else
        {
            System.out.println("You need to save more");
        }
    }
}
