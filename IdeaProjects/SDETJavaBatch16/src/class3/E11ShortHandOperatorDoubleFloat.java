package class3;

public class E11ShortHandOperatorDoubleFloat {
    public static void main(String[] args) {
        int number=0;
        //number=number+1;
        number++; //increase the value of number by one
        System.out.println(number);

        System.out.println("**********");

        double num=15.5;
        num++;
        System.out.println(number);
        System.out.println(num);

        System.out.println("**********");

        float f=11F;
        f/=2;
        System.out.println(f);
    }
}
