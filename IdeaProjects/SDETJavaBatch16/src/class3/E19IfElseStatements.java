package class3;

public class E19IfElseStatements {
    public static void main(String[] args) {
        //If inside parenthesis is True, Java will Print it, if False - ignore.
        int age=17;
        if(age>=18)
        {
            System.out.println("Time to study");
        }
        else
        {
            System.out.println("Time to work");
        }
    }
}
